def gaode_geocode(address):
    """调用高德地理编码 API"""
    params = urllib.parse.urlencode({
        "address": address,
        "key": GAODE_KEY,
        "city": "滨州",
    })
    url = f"https://restapi.amap.com/v3/geocode/geo?{params}"
    req = urllib.request.Request(url, headers={"User-Agent": "Mozilla/5.0"})
    resp = urllib.request.urlopen(req, timeout=15)
    data = json.loads(resp.read().decode("utf-8"))
    geocodes = data.get("geocodes", [])
    if geocodes:
        loc = geocodes[0].get("location", "")
        level = geocodes[0].get("level", "")
        if loc:
            lng, lat = loc.split(",")
            return [round(float(lng), 6), round(float(lat), 6)], level
    return None, None


def normalize_village_name(name):
    """统一村名格式：确保以'村'结尾"""
    name = name.strip()
    if not name:
        return None
    if not name.endswith("村") and not name.endswith("社区"):
        name += "村"
    return name


def main():
    # 加载现有数据
    with open(TOWNSHIPS_PATH, "r", encoding="utf-8") as f:
        existing = json.load(f)

    total_added = 0
    total_failed = 0
    total_skipped = 0

    for township, village_names in OFFICIAL_VILLAGES.items():
        county = TOWNSHIP_COUNTY.get(township)
        if not county:
            print(f"⚠️ {township}: 未找到所属区县，跳过")
            continue

        region = REGIONS[county]
        if township not in existing:
            print(f"⚠️ {township}: 不在 townships.json 中，跳过")
            continue

        existing_villages = set(existing[township].get("villages", {}).keys())
        official_set = set()
        for v in village_names:
            nv = normalize_village_name(v)
            if nv:
                official_set.add(nv)

        missing = official_set - existing_villages
        if not missing:
            print(f"✅ {township}: 全部 {len(official_set)} 个村已存在，无需补充")
            continue

        print(f"\n{'='*50}")
        print(f"📍 {county} {township}: 现有 {len(existing_villages)} 个村，"
              f"官方 {len(official_set)} 个，缺 {len(missing)} 个")
        print(f"{'='*50}")

        added = 0
        failed = 0
        for village_name in sorted(missing):
            address = f"{region['province']}{county}{township}{village_name}"
            time.sleep(0.3)
            coord, level = gaode_geocode(address)
            if coord:
                lng, lat = coord
                in_bounds = (region["lng_range"][0] <= lng <= region["lng_range"][1] and
                             region["lat_range"][0] <= lat <= region["lat_range"][1])
                if in_bounds:
                    existing[township]["villages"][village_name] = coord
                    added += 1
                    print(f"  ✅ {village_name}: {coord} (level={level})")
                else:
                    # 坐标超出范围，放宽一点再试
                    margin = 0.1
                    loosely_ok = (region["lng_range"][0] - margin <= lng <= region["lng_range"][1] + margin and
                                  region["lat_range"][0] - margin <= lat <= region["lat_range"][1] + margin)
                    if loosely_ok:
                        existing[township]["villages"][village_name] = coord
                        added += 1
                        print(f"  ⚠️ {village_name}: {coord} (稍偏，已接受)")
                    else:
                        failed += 1
                        print(f"  ❌ {village_name}: {coord} (坐标超出范围，跳过)")
            else:
                failed += 1
                print(f"  ❌ {village_name}: 未找到坐标")

        total_added += added
        total_failed += failed
        print(f"  → 补充 {added} 个, 失败 {failed} 个, "
              f"现有 {len(existing[township]['villages'])} 个村")

    # 保存结果
    with open(TOWNSHIPS_PATH, "w", encoding="utf-8") as f:
        json.dump(existing, f, ensure_ascii=False, indent=2)

    print(f"\n{'='*50}")
    print(f"全部完成！共补充 {total_added} 个村, 失败 {total_failed} 个")
    print(f"已写入: {TOWNSHIPS_PATH}")

    # 输出最终统计
    print(f"\n各乡镇村庄数量:")
    for tw in sorted(existing.keys()):
        count = len(existing[tw].get("villages", {}))
        county = TOWNSHIP_COUNTY.get(tw, "?")
        print(f"  {county} {tw}: {count} 个村")


if __name__ == "__main__":
    main()
